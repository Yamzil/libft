#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ss;
	int				i;

	ss = (unsigned char *) s;
	i = 0;
	while (n--)
	{
		ss[i] = 0;
		i++;
	}
}
